Console.WriteLine("Math.Min()");

int minValue = Math.Min(x, y);
Console.WriteLine($"the lowest value between {x} and {y} is {minValue}\n");

Console.WriteLine("Math.Sqrt()");

Console.WriteLine($"The square root of {x} is {Math.Sqrt(x)}");


